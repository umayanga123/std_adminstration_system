    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        editButton.setEnabled(true);
        addButton.setEnabled(true);
        deleteButton.setEnabled(true);
        backButton.setEnabled(true);
        saveButton.setEnabled(false);
        cancelButton.setEnabled(false);
        enableNavigation();
        disableFields();
        dateTimeTextField.setText(common.CommonMethods.getCurrentDateTime());
        Grade grade = new Grade(idTextField.getText(), nameTextField.getText(), teacherIds[gradeHeadComboBox.getSelectedIndex() - 1], Double.parseDouble(admFeeTextField.getText()), Double.parseDouble(SchoolFeeTextField.getText()), null, inputterTextField1.getText(), dateTimeTextField.getText());
        String msg = "Record " + idTextField.getText();
        try {
            if (mode.equals("add")) {
                res = GradeHandler.addGrade(grade);
                msg += " Added Successfully!";
            }
            if (mode.equals("edit")) {
                res = GradeHandler.updateGrade(grade);
                msg += " Updated Successfully!";
            }
            if (res == 1) {
                JOptionPane.showMessageDialog(this, msg);
                rs = GradeHandler.setNavigationResultSet("grade");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
        clearFields();
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Do you want to delete " + idTextField.getText() + "?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.YES_OPTION) {
            try {
                GradeHandler.deleteGrade(idTextField.getText());
                rs = GradeHandler.setNavigationResultSet("grade");
            } catch (SQLException ex) {
                Logger.getLogger(GradeScreen.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(GradeScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Grade - " + idTextField.getText() + " Deleted!");
            clearFields();
        }

    }//GEN-LAST:event_deleteButtonActionPerformed

