    private void showDataFields(Grade curGrd) {
        if (curGrd != null) {
            idTextField.setText(curGrd.getIdgrade());
            nameTextField.setText(curGrd.getName());
            gradeHeadComboBox.setSelectedIndex(tchIds.indexOf(new String(curGrd.getGradeHead())));
            admFeeTextField.setText(curGrd.getAdmissionFee() + "");
            SchoolFeeTextField.setText(curGrd.getSchoolFee() + "");
            inputterTextField1.setText(curGrd.getInputter());
            dateTimeTextField.setText(curGrd.getLastModified());

        }
    }

