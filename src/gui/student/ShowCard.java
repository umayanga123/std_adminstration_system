    private void fillForm(String idStudent) throws SQLException, ClassNotFoundException, ParseException, IOException {
        if (idStudent.length() > 0) {
            Student stu = StudentHandler.getStudent(idStudent);
            loadFields(stu);
        }
    }

    private void loadFields(Student st) throws ParseException, SQLException, ClassNotFoundException, IOException {
        if (st != null) {
            photoPath = "./img/profiles/" + st.getIdStudent() + "s.jpg";
            File fl = new File(photoPath);
            if (!fl.exists()) {
                JOptionPane.showMessageDialog(this, "Profile Picture not uploaded!");
            }
            pictureLabel1.setIcon(new ImageIcon(photoPath));
            barPath = "./img/bar/" + st.getIdStudent() + ".png";
            File f2 = new File(barPath);
            if (!f2.exists()) {
                try {
                    CommonMethods.outputtingBarcodeAsPNG(st.getIdStudent(), f2);
                } catch (BarcodeException ex) {
                    Logger.getLogger(ShowCard.class.getName()).log(Level.SEVERE, null, ex);
                } catch (OutputException ex) {
                    Logger.getLogger(ShowCard.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            barLabel.setIcon(new ImageIcon(barPath));
            studentComboBox.setSelectedItem(st.getIdStudent().toString());
            nameLabel.setText(st.getTitle() + " " + st.getFirstName() + " " + st.getLastName());
            dobLabel.setText(st.getAdmDate());
            addressLabel.setText("<html>" + st.getAdd1() + "<p>" + st.getAdd2() + "<p>" + st.getCity() + "<p>" + st.getPostalCode().toString() + "</html>");
            add = st.getAdd1() + "\n" + st.getAdd2() + "\n" + st.getCity() + "\n" + st.getPostalCode().toString();
        }
    }

