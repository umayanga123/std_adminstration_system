    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
        String reportSource = "./jasper/Card.jrxml";
        Map<String, Object> param = new HashMap<String, Object>();
        param.put("idStudent", idLabel.getText());
        param.put("name", nameLabel.getText());
        param.put("dob", dobLabel.getText());
        param.put("add1", add);
        param.put("proPath", photoPath);
        param.put("barPath", barPath);

        try {
            JasperReport jr = JasperCompileManager.compileReport(reportSource);
            JasperPrint jp = JasperFillManager.fillReport(jr, param, new JREmptyDataSource());
            //JasperViewer.viewReport(jp,false);
            JasperViewer jv = new JasperViewer(jp);
            // JasperViewer.viewReport(jp);
            ReportView rv = new ReportView(this, true, jv);
            rv.setVisible(true);

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_printButtonActionPerformed

